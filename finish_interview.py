def _build_timeline_data(data: Dict) -> Dict:
    """
    Extract per-question score, nervousness, and WPM from the session data dict.

    Returns:
        {
          q_labels : list[str]  — ["Q1", "Q2", ...]
          scores   : list[float] — per-question score on 0-10 scale
          nerv     : list[float] — per-question nervousness 0-1
          wpm      : list[float] — words-per-minute for each answer (or [] if unavailable)
          has_wpm  : bool
        }
    """
    q_labels: List[float] = []
    scores:   List[float] = []
    nerv:     List[float] = []
    wpm:      List[float] = []

    for i, (q, a, sc, em) in enumerate(
        zip(data["questions"], data["answers"], data["scores"], data["emotions"]), 1
    ):
        q_labels.append(f"Q{i}")

        # Score — normalise to 0-10
        raw_score = sc.get("score") or sc.get("total") or sc.get("overall")
        try:
            scores.append(round(float(raw_score), 1))
        except (TypeError, ValueError):
            scores.append(0.0)

        # Nervousness
        n_val = em.get("nervousness")
        nerv.append(round(float(n_val), 3) if n_val is not None else 0.0)

        # WPM — words in answer / (time_s / 60), fall back to word count only
        words = len((a or "").split())
        time_s = sc.get("time_s") or sc.get("time") or sc.get("duration_s")
        if time_s and float(time_s) > 5:
            wpm.append(round(words / (float(time_s) / 60), 1))
        else:
            wpm.append(0.0)

    return {
        "q_labels": q_labels,
        "scores":   scores,
        "nerv":     nerv,
        "wpm":      wpm,
        "has_wpm":  any(w > 0 for w in wpm),
        "n":        len(q_labels),
    }


def build_timeline_figure(tl: Dict) -> Optional[Any]:
    """
    Build a Plotly figure with 2-3 subplots:
      Row 1 (tall):   Score line  (0-10)
      Row 2 (medium): Nervousness area chart  (0-100%)
      Row 3 (medium): WPM bar chart (only if time data available)

    Aura AI dark theme: #020409 bg, neon-green / neon-cyan / violet lines.

    Returns the Plotly figure, or None if Plotly is unavailable.
    """
    if not PLOTLY_OK or tl["n"] < 1:
        return None

    rows = 3 if tl["has_wpm"] else 2
    row_heights = [0.50, 0.28, 0.22] if rows == 3 else [0.60, 0.40]
    subplot_titles = (["Score (0–10)", "Nervousness (%)", "Speaking Pace (WPM)"]
                      if rows == 3 else ["Score (0–10)", "Nervousness (%)"])

    fig = make_subplots(
        rows=rows, cols=1,
        shared_xaxes=True,
        vertical_spacing=0.07,
        row_heights=row_heights,
        subplot_titles=subplot_titles,
    )

    qs = tl["q_labels"]
    NEON_GREEN  = "#00ff88"
    NEON_CYAN   = "#00d4ff"
    VIOLET      = "#7c3aff"
    BG          = "#020409"
    BG_CARD     = "#0a1128"
    GRID        = "rgba(0,255,136,0.06)"
    TICK_COLOR  = "#2a4060"

    # ── Row 1: Score ──────────────────────────────────────────────────────────
    fig.add_trace(go.Scatter(
        x=qs, y=tl["scores"],
        mode="lines+markers+text",
        name="Score",
        line=dict(color=NEON_GREEN, width=2.5, shape="spline", smoothing=0.6),
        marker=dict(size=9, color=tl["scores"],
                    colorscale=[[0,"#ff3366"],[0.5,"#ffaa00"],[1,"#00ff88"]],
                    cmin=0, cmax=10,
                    line=dict(color=BG, width=1.5)),
        text=[f"{s}" for s in tl["scores"]],
        textposition="top center",
        textfont=dict(color=NEON_GREEN, size=9, family="Share Tech Mono"),
        fill="tozeroy",
        fillcolor="rgba(0,255,136,0.06)",
        hovertemplate="<b>%{x}</b><br>Score: %{y}/10<extra></extra>",
    ), row=1, col=1)

    # Target line at 7.5
    fig.add_hline(y=7.5, line=dict(color="rgba(0,255,136,0.25)", dash="dot", width=1),
                  row=1, col=1,
                  annotation_text="Target", annotation_font_color="rgba(0,255,136,0.5)",
                  annotation_font_size=8)

    # ── Row 2: Nervousness ────────────────────────────────────────────────────
    nerv_pct = [round(n * 100, 1) for n in tl["nerv"]]
    nerv_colors = []
    for n in tl["nerv"]:
        if n < 0.35:   nerv_colors.append("#00ff88")
        elif n < 0.65: nerv_colors.append("#ffaa00")
        else:          nerv_colors.append("#ff3366")

    fig.add_trace(go.Scatter(
        x=qs, y=nerv_pct,
        mode="lines+markers",
        name="Nervousness %",
        line=dict(color=NEON_CYAN, width=2, shape="spline", smoothing=0.5),
        marker=dict(size=8, color=nerv_colors,
                    line=dict(color=BG, width=1.5)),
        fill="tozeroy",
        fillcolor="rgba(0,212,255,0.06)",
        hovertemplate="<b>%{x}</b><br>Nervousness: %{y}%<extra></extra>",
    ), row=2, col=1)

    # Nervousness threshold lines
    for pct, label, alpha in [(35, "Low", "0.3"), (65, "High", "0.4")]:
        fig.add_hline(
            y=pct,
            line=dict(color=f"rgba(255,170,0,{alpha})", dash="dot", width=1),
            row=2, col=1,
        )

    # ── Row 3: WPM (if available) ─────────────────────────────────────────────
    if tl["has_wpm"] and rows == 3:
        wpm_colors = []
        for w in tl["wpm"]:
            if 120 <= w <= 160: wpm_colors.append("#00ff88")   # ideal
            elif 80 <= w <= 200: wpm_colors.append("#ffaa00")  # ok
            else:                wpm_colors.append("#ff3366")  # too fast/slow

        fig.add_trace(go.Bar(
            x=qs, y=tl["wpm"],
            name="WPM",
            marker=dict(
                color=wpm_colors,
                opacity=0.85,
                line=dict(color=BG, width=1),
            ),
            hovertemplate="<b>%{x}</b><br>WPM: %{y}<extra></extra>",
        ), row=3, col=1)

        # Ideal WPM band shading (120-160)
        fig.add_hrect(y0=120, y1=160,
                      fillcolor="rgba(0,255,136,0.05)",
                      line_width=0, row=3, col=1)

    # ── Global layout ─────────────────────────────────────────────────────────
    total_h = 340 if rows == 3 else 280

    fig.update_layout(
        height=total_h,
        paper_bgcolor=BG,
        plot_bgcolor=BG_CARD,
        font=dict(color=TICK_COLOR, family="Rajdhani, sans-serif", size=10),
        margin=dict(l=44, r=16, t=32, b=24),
        showlegend=False,
        hovermode="x unified",
        hoverlabel=dict(bgcolor="#0a1128", bordercolor="#00ff88",
                        font=dict(color="#b0cce8", size=10)),
        # Subplot title styling
        annotations=[dict(font=dict(color="#5a7098", size=9,
                                    family="Share Tech Mono")) for _
                     in fig.layout.annotations],
    )

    # Axis styling — apply to all subplots
    axis_style = dict(
        gridcolor=GRID,
        zerolinecolor="rgba(0,0,0,0)",
        tickfont=dict(color=TICK_COLOR, size=8),
        linecolor="rgba(0,0,0,0)",
    )
    fig.update_xaxes(**axis_style)
    fig.update_yaxes(**axis_style)
    fig.update_yaxes(range=[0, 10.5],  row=1, col=1)
    fig.update_yaxes(range=[0, 105],   row=2, col=1,
                     ticksuffix="%")

    return fig


def _timeline_chart_png(tl: Dict) -> Optional[bytes]:
    """
    Render the timeline chart as a PNG bytes object for PDF embedding.
    Returns None if Plotly or kaleido is unavailable.
    """
    if not PLOTLY_OK or not KALEIDO_OK:
        return None
    try:
        fig = build_timeline_figure(tl)
        if fig is None:
            return None
        img_bytes = fig.to_image(format="png", width=1040, height=360, scale=2)
        return img_bytes
    except Exception as exc:
        print(f"[finish_interview] Timeline chart PNG failed: {exc}")
        return None

