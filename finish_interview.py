def _weakness_summary(score_dict: Dict, q_type: str) -> str:
    """
    Condense the weakest NLP signals from the eval result into a bullet list
    used both in the UI preview and the Groq improvement prompt.
    """
    bullets: List[str] = []
    q_type_k = q_type.lower().strip()

    # 1. Missing STAR components (Behavioural / HR only)
    if q_type_k in ("behavioural", "hr"):
        star    = score_dict.get("star_scores", {})
        missing = [k for k, v in star.items() if not v]
        if missing:
            bullets.append(f"Missing STAR component(s): {', '.join(missing)}")

    # 2. Low relevance
    sim = score_dict.get("similarity_score")
    if isinstance(sim, (int, float)) and sim < 50:
        bullets.append("Answer drifted off-topic — needs stronger connection to the question")

    # 3. No keywords covered at all
    kw_defined = score_dict.get("kw_defined", False)
    kw_hits    = score_dict.get("keyword_hits", [])
    if kw_defined and len(kw_hits) == 0:
        bullets.append("None of the expected technical keywords were mentioned")

    # 4. Too short
    wc     = score_dict.get("word_count", 200)
    wc_min = {"technical": 80, "behavioural": 60, "hr": 50}.get(q_type_k, 60)
    if isinstance(wc, int) and wc < wc_min:
        bullets.append(f"Answer is too short ({wc} words) — needs more depth and examples")

    # 5. Too many filler words
    filler_ratio = score_dict.get("filler_ratio", 0)
    if isinstance(filler_ratio, float) and filler_ratio > 0.07:
        filler_count = score_dict.get("filler_count", "")
        bullets.append(
            f"Too many filler words ({filler_count} detected: um, like, basically) — "
            "remove them in the rewrite"
        )

    # 6. Grammar issues
    grammar_score = score_dict.get("grammar_score", 100)
    if isinstance(grammar_score, (int, float)) and grammar_score < 70:
        bullets.append("Grammar issues detected — fix sentence structure and punctuation")

    # 7. Negative framing
    sentiment = score_dict.get("sentiment_intensity", 0)
    if isinstance(sentiment, float) and sentiment < -0.3:
        bullets.append("Negative framing — reframe challenges as learning opportunities")

    # 8. Generic fallback
    if not bullets:
        score_val = score_dict.get("score")
        if isinstance(score_val, (int, float)) and score_val >= 4.0:
            bullets.append("Overall strong answer — minor polish for flow and conciseness")
        else:
            bullets.append("Overall structure could be tighter and more specific")

    return "\n".join(f"  • {b}" for b in bullets)


def _generate_improved_answer(
    answer:       str,
    question:     str,
    score_dict:   Dict,
    groq_api_key: str,
    q_type:       str = "technical",
) -> str:
    """
    Call Groq to rewrite the candidate's answer, fixing every weakness the
    evaluator identified while keeping their own examples and voice.

    Returns the improved text (str) or "" on failure.
    """
    if not groq_api_key:
        return ""
    if not answer or len(answer.strip()) < 10:
        return ""

    try:
        from groq import Groq
    except ImportError:
        log.warning("groq package not installed — cannot generate improved answer")
        return ""

    q_type_k  = q_type.lower().strip()
    wc_target = _WC_TARGETS.get(q_type_k, "120–200 words")
    weakness  = _weakness_summary(score_dict, q_type_k)

    type_instructions = {
        "technical": (
            "This is a TECHNICAL question. Improve by:\n"
            "  – Using precise technical terminology and correct concepts\n"
            "  – Mentioning relevant tools, frameworks, or algorithms where appropriate\n"
            "  – Adding at least one trade-off, edge case, or real-world consideration\n"
            "  – Avoiding STAR format (not required for technical answers)"
        ),
        "behavioural": (
            "This is a BEHAVIOURAL question. Improve by:\n"
            "  – Ensuring full STAR structure: Situation → Task → Action → Result\n"
            "  – Making the Situation specific (name a real project, team, or timeline)\n"
            "  – Making every Action first-person ('I did X', not 'we did X')\n"
            "  – Quantifying the Result ('reduced latency by 30%', 'led a team of 5')"
        ),
        "hr": (
            "This is an HR / self-reflection question. Improve by:\n"
            "  – Showing genuine self-awareness and career motivation\n"
            "  – Connecting personal values to the role\n"
            "  – Adding at least one specific concrete example to back up every claim\n"
            "  – Maintaining a confident, positive, professional tone throughout"
        ),
    }
    type_instr = type_instructions.get(q_type_k, type_instructions["technical"])

    system_msg = (
        "You are an expert interview coach rewriting a candidate's answer. You MUST:\n"
        "  1. Keep the candidate's own examples, projects, numbers, and domain knowledge\n"
        "  2. Fix every weakness listed — do not skip any\n"
        "  3. Maintain a natural first-person spoken tone (not overly polished or corporate)\n"
        "  4. Return ONLY the improved answer — no preamble, no labels, no markdown formatting"
    )
    user_msg = (
        f"Interview question:\n{answer[:400]}\n\n"
        f"Original candidate answer:\n{answer[:1200]}\n\n"
        f"Weaknesses to fix:\n{weakness}\n\n"
        f"{type_instr}\n\n"
        f"Target length: {wc_target}\n\n"
        "Write the improved answer now:"
    )
    # Fix: question text was being used twice — correct the user_msg
    user_msg = (
        f"Interview question:\n{question[:400]}\n\n"
        f"Original candidate answer:\n{answer[:1200]}\n\n"
        f"Weaknesses to fix:\n{weakness}\n\n"
        f"{type_instr}\n\n"
        f"Target length: {wc_target}\n\n"
        "Write the improved answer now:"
    )

    try:
        client = Groq(api_key=groq_api_key)
        resp   = client.chat.completions.create(
            model=_GROQ_MODEL_AI,
            messages=[
                {"role": "system", "content": system_msg},
                {"role": "user",   "content": user_msg},
            ],
            max_tokens=500,
            temperature=0.45,
        )
        result = resp.choices[0].message.content.strip()
        # Strip any accidental preamble like "Improved answer:"
        result = re.sub(
            r'^(improved\s+answer\s*:|answer\s*:)\s*', '', result, flags=re.IGNORECASE
        ).strip()
        log.info(f"Improved answer generated ({len(result.split())} words)")
        return result
    except Exception as exc:
        log.error(f"Improved answer generation failed: {exc}")
        return ""


def _render_answer_insight_panel(
    answer:       str,
    question:     str,
    score_dict:   Dict,
    groq_api_key: str,
    q_type:       str = "technical",
    panel_key:    str = "insight_0",
) -> None:
    """
    Render a Streamlit expander below each question card containing:
      • Score breakdown chips (Overall %, Relevance %, Keywords hit, STAR count)
      • Tab 1 — 💡 Ideal Answer  (pre-generated, stored in score_dict, zero API calls)
      • Tab 2 — ✨ Improved Version of YOUR answer  (lazy Groq rewrite, button-triggered,
                  cached in session_state["_improved_answers"][panel_key])
    """
    # ── Session cache init ────────────────────────────────────────────────────
    if "_improved_answers" not in st.session_state:
        st.session_state["_improved_answers"] = {}

    # ── Pull data from score_dict ─────────────────────────────────────────────
    ideal       = (
        score_dict.get("generated_ideal_answer") or
        score_dict.get("ideal_answer") or ""
    ).strip()
    score_val   = score_dict.get("score")
    similarity  = score_dict.get("similarity_score")
    kw_hits     = score_dict.get("keyword_hits", [])
    star_count  = score_dict.get("star_count")
    q_type_k    = q_type.lower().strip()
    q_type_lbl  = q_type_k.title()

    # ── Helper: chip colour ───────────────────────────────────────────────────
    def _cc(val, good=70, ok=45):
        if val is None:
            return _C["muted"]
        return _C["green"] if val >= good else _C["yellow"] if val >= ok else _C["red"]

    # ── Helper: hex → rgb triple for rgba() ──────────────────────────────────
    def _hex_rgb(h: str) -> str:
        h = h.lstrip("#")
        return ",".join(str(int(h[i:i+2], 16)) for i in (0, 2, 4))

    # ── Score chips HTML ─────────────────────────────────────────────────────
    chips_html = (
        '<div style="display:flex;flex-wrap:wrap;gap:.45rem;margin:.5rem 0 .9rem;">'
    )

    # Overall score chip
    if score_val is not None:
        try:
            pct = round(float(score_val) / 5.0 * 100)
            cc  = _cc(pct)
            chips_html += (
                f'<span style="background:rgba({_hex_rgb(cc)},.15);'
                f'border:1px solid {cc};color:{cc};border-radius:20px;'
                f'padding:.2rem .65rem;font-size:.68rem;font-weight:700;">'
                f'⭐ Score {pct}%</span>'
            )
        except Exception:
            pass

    # Relevance chip
    if similarity is not None:
        try:
            sv = float(similarity)
            cc = _cc(sv)
            chips_html += (
                f'<span style="background:rgba({_hex_rgb(cc)},.15);'
                f'border:1px solid {cc};color:{cc};border-radius:20px;'
                f'padding:.2rem .65rem;font-size:.68rem;font-weight:700;">'
                f'🎯 Relevance {round(sv)}%</span>'
            )
        except Exception:
            pass

    # Keywords chip
    if kw_hits:
        kw_str = ", ".join(kw_hits[:4]) + ("…" if len(kw_hits) > 4 else "")
        chips_html += (
            f'<span style="background:rgba({_hex_rgb(_C["teal"])},.15);'
            f'border:1px solid {_C["teal"]};color:{_C["teal"]};border-radius:20px;'
            f'padding:.2rem .65rem;font-size:.68rem;font-weight:700;">'
            f'🔑 Keywords: {kw_str}</span>'
        )

    # STAR chip (only for behavioural/hr)
    if q_type_k in ("behavioural", "hr") and star_count is not None:
        try:
            sc_int = int(star_count)
            sc_col = _C["green"] if sc_int >= 3 else _C["yellow"] if sc_int >= 2 else _C["red"]
            chips_html += (
                f'<span style="background:rgba({_hex_rgb(sc_col)},.15);'
                f'border:1px solid {sc_col};color:{sc_col};border-radius:20px;'
                f'padding:.2rem .65rem;font-size:.68rem;font-weight:700;">'
                f'📐 STAR {sc_int}/4</span>'
            )
        except Exception:
            pass

    chips_html += "</div>"

    # ── Expander ──────────────────────────────────────────────────────────────
    exp_label = f"💡 Ideal Answer & ✨ Improvement  ·  {q_type_lbl}"
    with st.expander(exp_label, expanded=False):

        st.markdown(chips_html, unsafe_allow_html=True)

        tab1, tab2 = st.tabs(["💡 Ideal Answer", "✨ Improved Version of Your Answer"])

        # ── Tab 1: Ideal Answer ───────────────────────────────────────────────
        with tab1:
            if ideal:
                st.markdown(
                    f'<div style="background:#030c1a;border-left:4px solid {_C["teal"]};'
                    f'border-radius:8px;padding:1rem 1.1rem;margin-top:.4rem;">'
                    f'<div style="font-size:.6rem;color:{_C["teal"]};text-transform:uppercase;'
                    f'letter-spacing:.1em;font-weight:700;margin-bottom:.55rem;">'
                    f'💡 IDEAL ANSWER — {q_type_lbl.upper()}</div>'
                    f'<div style="font-size:.82rem;color:{_C["text"]};line-height:1.7;'
                    f'white-space:pre-wrap;">{ideal}</div>'
                    f'<div style="font-size:.62rem;color:{_C["muted"]};margin-top:.75rem;">'
                    f'↑ This is what a top-scoring candidate would say. '
                    f'Use it as a reference to strengthen your own answer.</div>'
                    f'</div>',
                    unsafe_allow_html=True,
                )
            else:
                st.markdown(
                    f'<div style="color:{_C["muted"]};font-size:.78rem;padding:.5rem 0;">'
                    f'⚠️ Ideal answer not available for this question. '
                    f'Ensure a GROQ_API_KEY is set so the evaluator can pre-generate it.</div>',
                    unsafe_allow_html=True,
                )

        # ── Tab 2: Improved Version ───────────────────────────────────────────
        with tab2:
            cached = st.session_state["_improved_answers"].get(panel_key)

            if cached:
                # Already generated — show result + comparison toggle
                st.markdown(
                    f'<div style="background:#030c1a;border-left:4px solid {_C["accent"]};'
                    f'border-radius:8px;padding:1rem 1.1rem;margin-top:.4rem;">'
                    f'<div style="font-size:.6rem;color:{_C["accent"]};text-transform:uppercase;'
                    f'letter-spacing:.1em;font-weight:700;margin-bottom:.55rem;">'
                    f'✨ YOUR IMPROVED ANSWER</div>'
                    f'<div style="font-size:.82rem;color:{_C["text"]};line-height:1.7;'
                    f'white-space:pre-wrap;">{cached}</div>'
                    f'<div style="font-size:.62rem;color:{_C["muted"]};margin-top:.75rem;">'
                    f'↑ Your original answer rewritten to fix identified weaknesses, '
                    f'while keeping your own examples and knowledge.</div>'
                    f'</div>',
                    unsafe_allow_html=True,
                )

                st.markdown("<div style='margin-top:.6rem;'></div>",
                            unsafe_allow_html=True)
                if st.checkbox(
                    "🔍 Show side-by-side comparison",
                    key=f"{panel_key}_compare",
                ):
                    col_orig, col_imp = st.columns(2)
                    with col_orig:
                        st.markdown(
                            f'<div style="font-size:.6rem;color:{_C["muted"]};'
                            f'text-transform:uppercase;font-weight:700;margin-bottom:.3rem;">'
                            f'YOUR ORIGINAL</div>'
                            f'<div style="background:#080f20;border-radius:6px;padding:.75rem;'
                            f'font-size:.77rem;color:#94b0d8;line-height:1.65;'
                            f'white-space:pre-wrap;">{answer or "_(none)_"}</div>',
                            unsafe_allow_html=True,
                        )
                    with col_imp:
                        st.markdown(
                            f'<div style="font-size:.6rem;color:{_C["accent"]};'
                            f'text-transform:uppercase;font-weight:700;margin-bottom:.3rem;">'
                            f'IMPROVED VERSION</div>'
                            f'<div style="background:#080f20;border-radius:6px;padding:.75rem;'
                            f'font-size:.77rem;color:{_C["text"]};line-height:1.65;'
                            f'white-space:pre-wrap;">{cached}</div>',
                            unsafe_allow_html=True,
                        )

            else:
                # Not yet generated
                if not groq_api_key:
                    st.markdown(
                        f'<div style="color:{_C["muted"]};font-size:.78rem;padding:.5rem 0;">'
                        f'⚠️ A Groq API key is required to generate an improved version. '
                        f'Set the <code>GROQ_API_KEY</code> environment variable.</div>',
                        unsafe_allow_html=True,
                    )
                elif not answer or len(answer.strip()) < 10:
                    st.markdown(
                        f'<div style="color:{_C["muted"]};font-size:.78rem;padding:.5rem 0;">'
                        f'No answer was recorded for this question.</div>',
                        unsafe_allow_html=True,
                    )
                else:
                    st.markdown(
                        f'<div style="font-size:.78rem;color:{_C["muted"]};margin-bottom:.6rem;">'
                        f'Click below to generate an AI-improved version of your answer. '
                        f'Your original examples and knowledge are preserved — '
                        f'only the identified weaknesses are fixed.</div>',
                        unsafe_allow_html=True,
                    )

                    # Show weakness preview
                    weakness_text = _weakness_summary(score_dict, q_type_k)
                    weakness_html = weakness_text.replace("\n", "<br>")
                    st.markdown(
                        f'<div style="background:#0d0d1a;border-radius:6px;'
                        f'padding:.65rem .85rem;font-size:.72rem;color:{_C["yellow"]};'
                        f'margin-bottom:.75rem;border:1px solid rgba(255,184,64,.18);">'
                        f'<b style="font-size:.65rem;text-transform:uppercase;'
                        f'letter-spacing:.08em;">Weaknesses that will be fixed:</b>'
                        f'<br>{weakness_html}</div>',
                        unsafe_allow_html=True,
                    )

                    if st.button(
                        "✨ Generate Improved Answer",
                        key=f"{panel_key}_gen_btn",
                        use_container_width=True,
                    ):
                        with st.spinner("Rewriting your answer…"):
                            improved = _generate_improved_answer(
                                answer       = answer,
                                question     = question,
                                score_dict   = score_dict,
                                groq_api_key = groq_api_key,
                                q_type       = q_type_k,
                            )
                        if improved:
                            st.session_state["_improved_answers"][panel_key] = improved
                            st.rerun()
                        else:
                            st.error(
                                "Could not generate an improved answer. "
                                "Check your API key or network and try again."
                            )


