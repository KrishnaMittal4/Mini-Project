#  MARKDOWN EXPORT  —  Notion / Obsidian compatible  (v2.1)
# ═══════════════════════════════════════════════════════════════════════════════

def _build_markdown_export(data: Dict) -> str:
    """
    Build a Notion / Obsidian-compatible Markdown string from the session data
    dict produced by _collect_session_data().

    Structure
    ---------
    • YAML frontmatter  — tags, date, score, role (Obsidian Dataview ready)
    • Session summary   — Notion [!summary] callout
    • Performance table — score / nervousness / emotion per question
    • Per-question H2   — metadata callout, answer, score table, STAR ✅/❌ or
                          keyword hit/miss, coaching feedback, ideal answer
                          [!tip] callout, improved version [!abstract] callout,
                          follow-up probes as H4 sub-sections
    • Emotion summary   — distribution table + voice metrics
    • RL session summary (if present)
    • Notes             — blank section for candidate's own reflections
    """
    lines: List[str] = []
    a = lines.append

    candidate    = data.get("candidate", "Candidate")
    role         = data.get("job_role", "")
    timestamp    = data.get("timestamp", datetime.now().strftime("%d %B %Y, %H:%M"))
    date_iso     = datetime.now().strftime("%Y-%m-%d")
    duration     = data.get("duration")
    avg_score    = data.get("avg_score")
    avg_nerv     = data.get("avg_nerv")
    verdict      = data.get("verdict", "—")
    dom_emotion  = data.get("dominant_emotion", "—")
    n_questions  = data.get("n_questions", 0)

    questions    = data.get("questions", [])
    answers      = data.get("answers", [])
    scores       = data.get("scores", [])
    emotions     = data.get("emotions", [])
    rl_report    = data.get("rl_report", {})
    fu_records   = data.get("follow_up_records", [])
    improved_map = data.get("improved_answers", {})
    sa_list      = [s for s in data.get("session_answers", [])
                    if not s.get("is_follow_up", False)]

    # ── inline helpers ────────────────────────────────────────────────────────

    def _md_nerv_label(n: float) -> str:
        if n < 0.35:  return "Low"
        if n < 0.55:  return "Low-Moderate"
        if n < 0.75:  return "Moderate"
        return "High"

    def _md_fmt_dur(s) -> str:
        if not s:
            return "—"
        m, sec = divmod(int(s), 60)
        h, m   = divmod(m, 60)
        return f"{h}h {m}m {sec}s" if h else f"{m}m {sec}s"

    def _score_emoji(v) -> str:
        try:
            v = float(v)
            return "🟢" if v >= 7.5 else "🟡" if v >= 5.0 else "🔴"
        except (TypeError, ValueError):
            return "⬜"

    def _nerv_emoji(n) -> str:
        try:
            n = float(n)
            return "🟢" if n < 0.35 else "🟡" if n < 0.65 else "🔴"
        except (TypeError, ValueError):
            return "⬜"

    def _trunc(text: str, limit: int = 1200) -> str:
        text = (text or "").strip()
        if len(text) <= limit:
            return text
        return text[:limit] + f"\n\n*(truncated at {limit} chars — full text in PDF report)*"

    # ── YAML frontmatter (Obsidian) ───────────────────────────────────────────
    a("---")
    tag_role = role.lower().replace(" ", "-") if role else ""
    a(f"tags: [aura-ai, interview{', ' + tag_role if tag_role else ''}]")
    a(f"date: {date_iso}")
    if avg_score is not None:
        a(f"score: {avg_score}")
    if role:
        a(f"role: \"{role}\"")
    a(f"candidate: \"{candidate}\"")
    a(f"verdict: \"{verdict}\"")
    a("---")
    a("")

    # ── Title ─────────────────────────────────────────────────────────────────
    title = f"Aura AI Interview Report — {candidate}"
    if role:
        title += f" · {role}"
    a(f"# {title}")
    a("")
    a(f"*Generated {timestamp}*")
    a("")

    # ── Session summary callout ───────────────────────────────────────────────
    a("> [!summary] Session summary")
    score_str = f"{avg_score:.1f}/10" if avg_score is not None else "—"
    a(f"> **Overall score:** {score_str}  |  **Verdict:** {verdict}  |  **Questions:** {n_questions}")
    if avg_nerv is not None:
        try:
            a(f"> **Avg nervousness:** {int(float(avg_nerv)*100)}%"
              f" ({_md_nerv_label(float(avg_nerv))})")
        except (TypeError, ValueError):
            a("> **Avg nervousness:** —")
    a(f"> **Dominant emotion:** {dom_emotion}  |  **Duration:** {_md_fmt_dur(duration)}")
    a("")

    # ── Performance overview table ────────────────────────────────────────────
    a("## Performance overview")
    a("")
    a("| # | Question | Score | Nervousness | Emotion |")
    a("|---|---|---|---|---|")
    for i, (q, sc, em) in enumerate(zip(questions, scores, emotions), 1):
        sv = None
        try:
            sv = float(sc.get("score") or sc.get("total") or sc.get("overall") or 0)
            # normalise: stored 0-5 → display 0-10
            sv_disp = sv * 2 if sv <= 5.0 else sv
            score_cell = f"{_score_emoji(sv_disp)} {sv_disp:.1f}/10"
        except (TypeError, ValueError):
            score_cell = "—"

        nv = em.get("nervousness")
        try:
            nerv_cell = f"{_nerv_emoji(nv)} {int(float(nv)*100)}%"
        except (TypeError, ValueError):
            nerv_cell = "—"

        q_short = (q[:68] + "…") if len(q) > 68 else q
        q_short = q_short.replace("|", "\\|")
        a(f"| Q{i} | {q_short} | {score_cell} | {nerv_cell} | {em.get('dominant', '—')} |")
    a("")

    # ── Per-question sections ─────────────────────────────────────────────────
    for i, (q, ans, sc, em) in enumerate(zip(questions, answers, scores, emotions), 1):
        sa = sa_list[i - 1] if i - 1 < len(sa_list) else {}

        # score — normalise to /10 display
        try:
            sv_raw = float(sc.get("score") or sc.get("total") or sc.get("overall") or 0)
            sv_10  = round(sv_raw * 2, 1) if sv_raw <= 5.0 else round(sv_raw, 1)
            score_display = f"{sv_10}/10"
        except (TypeError, ValueError):
            sv_raw = None
            sv_10  = None
            score_display = "—"

        q_type     = (sa.get("type") or sa.get("question_type") or
                      sc.get("question_type") or "").lower().strip()
        q_diff     = (sa.get("difficulty") or sc.get("difficulty") or "").lower().strip()
        if not q_diff or "all" in q_diff:
            q_diff = "medium"

        dom_face   = sa.get("emotion") or em.get("dominant", "")
        dom_voice  = sa.get("voice_emotion", "")
        nerv_raw   = (sa.get("nervousness") if sa.get("nervousness") is not None
                      else em.get("nervousness"))
        try:
            nerv_str = (f"{int(float(nerv_raw)*100)}%"
                        f" ({_md_nerv_label(float(nerv_raw))})")
        except (TypeError, ValueError):
            nerv_str = "—"

        feedback   = (sa.get("feedback") or sc.get("feedback") or
                      sc.get("comment") or "").strip()
        rl_action  = sa.get("rl_next_action", sc.get("rl_next_action", ""))

        nlp_pct    = float(sa.get("final_score",      sc.get("final_score",      0)) or 0)
        rel_pct    = float(sa.get("similarity_score", sc.get("similarity_score", 0)) or 0)
        grm_pct    = float(sa.get("grammar_score",    sc.get("grammar_score",    0)) or 0)
        depth_sc   = float(sa.get("depth_score",      sc.get("depth_score",      0)) or 0)
        fluency_sc = float(sa.get("fluency_score",    sa.get("fluency", 3.5))        or 3.5)
        word_cnt   = int(sa.get("word_count",   sc.get("word_count",   0)) or 0)
        filler_cnt = int(sa.get("filler_count", sc.get("filler_count", 0)) or 0)

        star_dict  = sa.get("star", sc.get("star_scores", {}))
        kw_hits    = sa.get("keyword_hits", sc.get("keyword_hits", []))
        kw_all     = sa.get("keywords",     sc.get("keywords",     []))
        kw_miss    = [k for k in kw_all if k not in kw_hits]

        ideal_txt   = (sa.get("generated_ideal_answer") or sc.get("generated_ideal_answer") or
                       sa.get("ideal_answer")            or sc.get("ideal_answer") or "").strip()
        improved_txt = improved_map.get(f"insight_q{i}", "").strip()

        # ── Question heading ──────────────────────────────────────────────────
        a("---")
        a("")
        a(f"## Q{i} — {q}")
        a("")

        # metadata callout
        a("> [!info] Question metadata")
        if q_type:
            a(f"> **Type:** {q_type.title()}  ")
        if q_diff:
            a(f"> **Difficulty:** {q_diff.title()}  ")
        a(f"> **Score:** {_score_emoji(sv_10)} {score_display}  ")
        if dom_face:
            a(f"> **Face emotion:** {dom_face}  ")
        if dom_voice:
            a(f"> **Voice emotion:** {dom_voice}  ")
        a(f"> **Nervousness:** {nerv_str}  ")
        if rl_action and rl_action not in ("static", ""):
            a(f"> **RL action:** `{rl_action}`  ")
        a("")

        # ── Answer ────────────────────────────────────────────────────────────
        a("### Your answer")
        a("")
        a(_trunc(ans) if ans and ans.strip() else "*(no answer recorded)*")
        a("")

        # ── Score breakdown table ─────────────────────────────────────────────
        a("### Score breakdown")
        a("")
        a("| Metric | Value |")
        a("|---|---|")
        a(f"| Overall score | {score_display} |")
        if nlp_pct:   a(f"| NLP score   | {round(nlp_pct)}% |")
        if rel_pct:   a(f"| Relevance   | {round(rel_pct)}% |")
        if grm_pct:   a(f"| Grammar     | {round(grm_pct)}% |")
        if depth_sc:  a(f"| Depth       | {depth_sc:.1f}/5 |")
        if fluency_sc: a(f"| Fluency     | {fluency_sc:.1f}/5 |")
        a(f"| Word count  | {word_cnt} |")
        a(f"| Filler words | {filler_cnt} |")
        a("")

        # ── STAR (behavioural / HR) or keywords (technical) ───────────────────
        is_beh = q_type in ("behavioural", "behavioral", "hr", "")
        if is_beh and star_dict:
            a("### STAR analysis")
            a("")
            for comp in ["Situation", "Task", "Action", "Result"]:
                mark = "✅" if star_dict.get(comp) else "❌"
                a(f"- {mark} **{comp}**")
            a("")
        elif not is_beh and (kw_hits or kw_miss):
            a("### Keyword coverage")
            a("")
            if kw_hits:
                a("**Hit:** " + ", ".join(f"`{k}`" for k in kw_hits))
                a("")
            if kw_miss:
                a("**Missing:** " + ", ".join(f"`{k}`" for k in kw_miss))
                a("")

        # ── Coaching feedback ─────────────────────────────────────────────────
        if feedback:
            a("### Coaching feedback")
            a("")
            a(f"> {feedback}")
            a("")

        # ── Ideal answer ──────────────────────────────────────────────────────
        if ideal_txt:
            a("### Ideal answer")
            a("")
            a("> [!tip] Reference answer")
            for ln in ideal_txt.splitlines():
                a(f"> {ln}" if ln.strip() else ">")
            a(">")
            a("> *Use this as a benchmark — a top-scoring candidate would cover these points.*")
            a("")

        # ── Improved version ──────────────────────────────────────────────────
        if improved_txt:
            a("### Improved version of your answer")
            a("")
            a("> [!abstract] AI-improved version")
            for ln in improved_txt.splitlines():
                a(f"> {ln}" if ln.strip() else ">")
            a(">")
            a("> *Your original answer rewritten to fix identified weaknesses.*")
            a("")

        # ── Follow-up probes (follow_up_engine records) ───────────────────────
        fu_for_q = [r for r in fu_records if r.q_index == i - 1]
        for fu in fu_for_q:
            a(f"#### ↳ Follow-up — {fu.probe_label}")
            a("")
            a(f"*{fu.follow_up_q}*")
            a("")
            a(_trunc(fu.follow_up_answer) if fu.follow_up_answer and
              fu.follow_up_answer.strip() else "*(no answer recorded)*")
            a("")
            fu_s10 = round(fu.score * 2, 1)
            delta  = fu.depth_delta
            delta_str = f"+{delta:.1f}" if delta > 0 else f"{delta:.1f}"
            a(f"**Follow-up score:** {fu_s10}/10  |  "
              f"**Depth Δ:** {delta_str}  |  "
              f"**Nervousness:** {int(fu.nervousness*100)}%")
            if fu.feedback:
                a("")
                a(f"> {fu.feedback}")
            a("")

        # ── RL follow-ups from session_answers ────────────────────────────────
        rl_fu_list = [s for s in data.get("session_answers", [])
                      if s.get("is_follow_up") and s.get("parent_q_number") == i]
        for rl_fu in rl_fu_list:
            a(f"#### ↳ RL Follow-up — {rl_fu.get('probe_label', 'Follow-up probe')}")
            a("")
            fu_ans = (rl_fu.get("answer") or "").strip()
            a(_trunc(fu_ans) if fu_ans else "*(no answer recorded)*")
            a("")
            if rl_fu.get("feedback"):
                a(f"> {rl_fu['feedback']}")
                a("")

    # ── Emotion & voice summary ───────────────────────────────────────────────
    a("---")
    a("")
    a("## Emotion & voice summary")
    a("")

    nerv_summary = data.get("nerv_summary", {})
    dist = nerv_summary.get("distribution", {})
    if dist:
        a("### Emotion distribution")
        a("")
        a("| Emotion | % of session |")
        a("|---|---|")
        for emo, pct in sorted(dist.items(), key=lambda x: -x[1]):
            a(f"| {emo} | {pct}% |")
        a("")

    voice_wpm    = nerv_summary.get("avg_wpm")
    voice_filler = nerv_summary.get("filler_ratio")
    if voice_wpm or voice_filler:
        a("### Voice metrics")
        a("")
        if voice_wpm:
            a(f"- **Average WPM:** {round(float(voice_wpm))}")
        if voice_filler:
            a(f"- **Filler word ratio:** {round(float(voice_filler)*100, 1)}%")
        a("")

    # ── RL session summary ────────────────────────────────────────────────────
    if rl_report:
        a("---")
        a("")
        a("## RL session summary")
        a("")
        a("> [!note] Adaptive sequencer")
        a(f"> **Total steps:** {rl_report.get('total_steps', 0)}  |  "
          f"**Final ε:** {rl_report.get('epsilon', '—')}")
        action_dist = rl_report.get("action_distribution", {})
        if action_dist:
            a(">")
            a("> **Action distribution:**")
            for act, count in sorted(action_dist.items(), key=lambda x: -x[1]):
                a(f"> - `{act}`: {count}")
        a("")

    # ── Notes (blank — for candidate's own use) ───────────────────────────────
    a("---")
    a("")
    a("## Notes")
    a("")
    a("*Add your own reflections here after reviewing the report.*")
    a("")
    a("- What did I do well?")
    a("- What would I change?")
    a("- Topics to study before the real interview:")
    a("")

    # ── Footer ────────────────────────────────────────────────────────────────
    a("---")
    a("")
    a("*Generated by Aura AI — Research-backed multimodal interview coach*  ")
    a("*Emotion: DeepFace + MediaPipe · NLP: Groq LLaMA-3.3-70b · "
      "RL sequencer: Q-learning (Patel et al. 2023)*")
    a("")

    return "\n".join(lines)


def _build_plain_text_fallback(data: Dict) -> str:
    """Minimal plain-text report when ReportLab is not installed."""
    lines = [
        "=" * 70,
        "AURA AI — INTERVIEW REPORT",
        "=" * 70,
        f"Candidate : {data['candidate']}",
        f"Role      : {data['job_role'] or '—'}",
        f"Date/Time : {data['timestamp']}",
        f"Duration  : {_fmt_duration(data['duration']) if data['duration'] else '—'}",
        "",
        f"Overall Score  : {data['avg_score']}/10  ({data['verdict']})" if data['avg_score'] else "Overall Score  : —",
        f"Avg Nervousness: {int((data['avg_nerv'] or 0)*100)}%  ({_nerv_label(data['avg_nerv'] or 0)})" if data['avg_nerv'] else "Avg Nervousness: —",
        f"Dominant Emotion: {data['dominant_emotion'] or '—'}",
        "",
        "-" * 70,
        "QUESTION-BY-QUESTION BREAKDOWN",
        "-" * 70,
    ]
    for i, (q, a, sc, em) in enumerate(
        zip(data['questions'], data['answers'], data['scores'], data['emotions']), 1
    ):
        score_val = sc.get("score") or sc.get("total") or sc.get("overall")
        lines.append(f"\nQ{i}: {q}")
        lines.append(f"Answer: {a or '(no answer)'}")
        if score_val is not None:
            lines.append(f"Score: {score_val}/10")
        fb = sc.get("feedback") or sc.get("comment") or ""
        if fb:
            lines.append(f"Feedback: {fb}")
        lines.append("")
    lines += ["=" * 70, "Generated by Aura AI", "=" * 70]
    return "\n".join(lines)


