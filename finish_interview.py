def _pct_bar_html(value: float, colour: str, width: int = 100) -> str:
    """Return an HTML mini progress bar."""
    filled = int(min(max(value, 0), 1) * width)
    return (
        f'<div style="background:rgba(90,120,200,.12);border-radius:4px;'
        f'height:6px;width:{width}px;display:inline-block;vertical-align:middle;">'
        f'<div style="background:{colour};border-radius:4px;height:6px;width:{filled}px;"></div>'
        f'</div>'
    )


def _fmt_duration(seconds: float) -> str:
    m, s = divmod(int(seconds), 60)
    h, m = divmod(m, 60)
    if h:
        return f"{h}h {m}m {s}s"
    return f"{m}m {s}s"


def _collect_session_data(ss: Any) -> Dict:
    """
    Pull all relevant data from Streamlit session_state.
    Returns a unified dict used by both the Streamlit report and the PDF.
    """
    questions: List[str] = ss.get("interview_questions", [])
    answers:   List[str] = ss.get("interview_answers",   [])
    scores:    List[Dict] = ss.get("answer_scores",      [])
    emotions:  List[Dict] = ss.get("emotion_history",    [])

    # Pad shorter lists so zip works safely
    n = max(len(questions), len(answers))
    while len(questions) < n: questions.append("")
    while len(answers)   < n: answers.append("")
    while len(scores)    < n: scores.append({})
    while len(emotions)  < n: emotions.append({})

    # --- Aggregate scores ---
    numeric_scores = []
    for sc in scores:
        v = sc.get("score") or sc.get("total") or sc.get("overall")
        if v is not None:
            try:
                numeric_scores.append(float(v))
            except (TypeError, ValueError):
                pass

    avg_score = round(sum(numeric_scores) / len(numeric_scores), 1) if numeric_scores else None

    # --- Nervousness summary ---
    vp = ss.get("voice_pipeline")
    nerv_summary: Dict = {}
    if vp and hasattr(vp, "get_session_summary"):
        try:
            nerv_summary = vp.get_session_summary() or {}
        except Exception:
            pass

    avg_nerv = nerv_summary.get("nervousness")
    if avg_nerv is None:
        # Fall back: average over emotion_history snapshots
        nerv_vals = [e.get("nervousness") for e in emotions if e.get("nervousness") is not None]
        avg_nerv  = round(sum(nerv_vals) / len(nerv_vals), 3) if nerv_vals else None

    # --- Dominant emotion ---
    dominant_emotion = nerv_summary.get("dominant", "")
    if not dominant_emotion and emotions:
        from collections import Counter
        doms = [e.get("dominant", "") for e in emotions if e.get("dominant")]
        if doms:
            dominant_emotion = Counter(doms).most_common(1)[0][0]

    # --- Overall verdict ---
    verdict = "—"
    if avg_score is not None:
        if avg_score >= 8.0:
            verdict = "Excellent"
        elif avg_score >= 6.5:
            verdict = "Good"
        elif avg_score >= 5.0:
            verdict = "Average"
        else:
            verdict = "Needs Improvement"

    return {
        "candidate":        ss.get("candidate_name", "Candidate"),
        "job_role":         ss.get("job_role", ""),
        "timestamp":        datetime.now().strftime("%d %B %Y, %H:%M"),
        "duration":         ss.get("session_duration_s"),
        "questions":        questions,
        "answers":          answers,
        "scores":           scores,
        "emotions":         emotions,
        "avg_score":        avg_score,
        "avg_nerv":         avg_nerv,
        "dominant_emotion": dominant_emotion,
        "nerv_summary":     nerv_summary,
        "verdict":          verdict,
        "n_questions":      n,
        # ── Rich per-answer + RL data for enhanced PDF sections ───────────────
        "session_answers":  ss.get("session_answers", []),
        "rl_report":        ss.get("rl_report", {}),
        "follow_up_records": ss.get("follow_up_records", []),  # v9.2
        # v2.0: improved answers generated via the insight panel ─────────────
        # keyed as "insight_q1", "insight_q2", … matching panel_key in the UI
        "improved_answers": ss.get("_improved_answers", {}),
    }




