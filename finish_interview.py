def render_finish_button(
    label: str = "🏁 Finish Interview",
    key: str   = "btn_finish_interview",
) -> None:
    """
    Renders the "Finish Interview" button.
    When clicked, sets session_state["interview_finished"] = True and reruns.

    Usage (at the bottom of your interview page):
        from finish_interview import render_finish_button
        render_finish_button()
    """
    st.markdown("<br>", unsafe_allow_html=True)

    col_l, col_c, col_r = st.columns([1, 2, 1])
    with col_c:
        # Custom styled button via HTML + a real Streamlit button underneath
        st.markdown(
            f"""
<style>
div[data-testid="stButton"] > button#{key} {{
  width: 100%;
  background: linear-gradient(135deg, #7c6bff, #3b8bff);
  color: white;
  border: none;
  border-radius: 12px;
  padding: 0.75rem 2rem;
  font-size: 1rem;
  font-weight: 800;
  letter-spacing: .04em;
  cursor: pointer;
  transition: opacity .2s, transform .15s;
}}
div[data-testid="stButton"] > button#{key}:hover {{
  opacity: .87;
  transform: translateY(-2px);
}}
</style>
""",
            unsafe_allow_html=True,
        )
        if st.button(label, key=key, use_container_width=True):
            st.session_state["interview_finished"] = True
            # Capture end-time for duration tracking (if start_time was set)
            if "interview_start_time" in st.session_state:
                import time as _time
                elapsed = _time.time() - st.session_state["interview_start_time"]
                st.session_state["session_duration_s"] = elapsed
            st.rerun()


def render_final_report(ss: Any = None) -> bool:
    """
    Check if the interview is finished; if so, render the full report and
    a PDF download button, then return True.
    Returns False when the interview is still in progress (nothing rendered).

    Usage (at the TOP of your interview page function):
        from finish_interview import render_final_report
        if render_final_report(st.session_state):
            return      # stop rendering interview UI

    Args:
        ss: pass st.session_state explicitly, or leave None to use the global.
    """
    if ss is None:
        ss = st.session_state

    if not ss.get("interview_finished", False):
        return False

    # ── Collect all data ──────────────────────────────────────────────────────
    data = _collect_session_data(ss)

    # ── Render Streamlit report ───────────────────────────────────────────────
    _render_streamlit_report(data)

    # ── PDF download ──────────────────────────────────────────────────────────
    st.markdown("<br>", unsafe_allow_html=True)
    st.markdown(
        f'<div style="text-align:center;font-size:.8rem;color:{_C["muted"]};">'
        f'Download a formatted copy of this report</div>',
        unsafe_allow_html=True,
    )

    pdf_bytes = _build_pdf(data)

    if REPORTLAB_OK:
        mime      = "application/pdf"
        filename  = f"aura_interview_report_{data['candidate'].replace(' ','_')}.pdf"
        btn_label = "⬇️  Download PDF Report"
    else:
        # Fallback: plain text
        mime      = "text/plain"
        filename  = f"aura_interview_report_{data['candidate'].replace(' ','_')}.txt"
        btn_label = "⬇️  Download Report (TXT)"
        st.warning(
            "⚠️ ReportLab is not installed — downloading as plain text instead.  "
            "Run `pip install reportlab` for PDF output."
        )

    # ── Build Markdown export ─────────────────────────────────────────────────
    md_bytes    = _build_markdown_export(data).encode("utf-8")
    md_filename = (
        f"aura_interview_{data['candidate'].replace(' ', '_')}"
        f"_{datetime.now().strftime('%Y%m%d')}.md"
    )

    # ── Two download buttons side by side ─────────────────────────────────────
    col_pdf, col_md = st.columns(2)

    with col_pdf:
        st.download_button(
            label=btn_label,
            data=pdf_bytes,
            file_name=filename,
            mime=mime,
            key="dl_pdf_report",
            use_container_width=True,
        )

    with col_md:
        st.markdown(
            f'<div style="text-align:center;font-size:.72rem;color:{_C["muted"]};'
            f'margin-bottom:.3rem;">Notion / Obsidian compatible</div>',
            unsafe_allow_html=True,
        )
        st.download_button(
            label="📝  Export to Notion / Obsidian (.md)",
            data=md_bytes,
            file_name=md_filename,
            mime="text/markdown",
            key="dl_md_report",
            use_container_width=True,
        )

    # ── Restart option ────────────────────────────────────────────────────────
    st.markdown("<br>", unsafe_allow_html=True)
    col_l2, col_c2, col_r2 = st.columns([1, 2, 1])
    with col_c2:
        if st.button("🔄  Start New Interview", key="btn_new_interview",
                     use_container_width=True):
            # Clear interview-related keys (preserves model/pipeline state)
            for k in [
                "interview_finished", "interview_questions", "interview_answers",
                "answer_scores", "emotion_history", "session_duration_s",
                "interview_start_time", "current_question_idx",
                "transcribed_text", "last_audio_id",
            ]:
                ss.pop(k, None)
            # Reset voice pipeline session if present
            vp = ss.get("voice_pipeline")
            if vp and hasattr(vp, "reset_session"):
                try:
                    vp.reset_session()
                except Exception:
                    pass
            st.rerun()

    return True